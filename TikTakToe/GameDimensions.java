package working_projects.TikTakToe;

public class GameDimensions {
    private final int width;
    private final int height;

    public GameDimensions(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
